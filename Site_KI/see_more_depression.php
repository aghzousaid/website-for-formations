        Un Français sur cinq a déjà souffert ou souffrira d’une dépression au cours de sa vie. <br>
        Vous pensez être à l’abri de cet état car vos proches vous considèrent comme une personne forte mentalement ? 
        Sachez que la dépression nous concerne tous.
        <br><br>

        La dépression n’est pas un simple coup de blues. Loin d’être passagère, la dépression est un état exacerbé 
        d’humeurs négatives ayant une influence sur la vie du malade. En effet, la dépression peut provoquer un repli sur soi, 
        une perte d’intérêt et de plaisir, un sentiment de culpabilité, de dévalorisation, voire des idées suicidaires pouvant  
        même être récurrentes.<br> <br>

        Chez les personnes diagnostiquées dépressives, le risque de suicide est 30 fois supérieur à la population générale.<br> 
        La dépression représente ainsi la première cause de suicide.<br><br>

        Cette maladie psychiatrique chronique due à une diminution de l’activité de plusieurs neurotransmetteurs entraîne 
        une grande souffrance, aussi bien au dépressif qu’à son entourage, et induit notamment un comportement autodestructeur.<br><br>

        En revanche, les causes exactes de la dépression sont inconnues mais elle peut être provoquée à la suite d’événements bouleversants 
        et situations de la vie extrêmement stressantes : mort d’un être cher, difficultés financières, isolement social, conflits relationnels 
        avec un conjoint ou la famille, environnement de travail stressant, problèmes de santé.<br><br>

        Selon l’Organisation Mondiale de la Santé (OMS), les troubles mentaux constituent une des plus lourdes charges
        en termes de morbidité dans le monde. De plus, parmi les 18-25 ans, 7,5 % de la population a vécu un épisode dépressif
        caractérisé dans les 12 derniers mois.<br><br>

        Il est donc impératif de diagnostiquer un malade dépressif au plus vite afin d’optimiser la prise en charge et limiter
        toutes conséquences.<br><br>

        C’est pourquoi, l’organisme Kamedis Institut propose aux médecins généralistes une formation DPC  qualifiante : <br>
        « Diagnostiquer la dépression ».<br><br>

        Cette formation a pour vocation de vous former aux repérages des signes cliniques caractéristiques d’un état dépressif et d’améliorer 
        vos pratiques en matière de diagnostic.<br><br>


