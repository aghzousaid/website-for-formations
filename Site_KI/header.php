    <script type="text/javascript">

        function validate_E_learning(){
            if (document.getElementById('responsetwo').checked) {
                document.getElementById('choix_dpc').style.display ="block";
                document.getElementById('form_modal_div_dpc_non').style.display= "none";
                document.getElementById('form_modal_div_dpc_oui').style.display= "none";
                document.getElementById('form_modal_div').style.display ="none";
            } else {
                document.getElementById('choix_dpc').style.display ="none";
            }
        }

        function validate_DPC(){
            if (document.getElementById('responseone').checked) {
                document.getElementById('choix_dpc').style.display= "none";
                document.getElementById('form_modal_div_dpc_non').style.display= "none";
                document.getElementById('form_modal_div_dpc_oui').style.display= "none";
                document.getElementById('form_modal_div').style.display ="block";
            } else {
                document.getElementById('form_modal_div').style.display ="none";
            }
        }


        function non_dpc(){
            if (document.getElementById('non_dpc').checked) {
                document.getElementById('form_modal_div_dpc_non').style.display ="block";
                document.getElementById('form_modal_div_dpc_oui').style.display ="none";
            } else {
                document.getElementById('form_modal_div_dpc_non').style.display ="none";
            }
        }

        function oui_dpc(){
            if (document.getElementById('oui_dpc').checked) {
                document.getElementById('form_modal_div_dpc_non').style.display= "none";
                document.getElementById('form_modal_div_dpc_oui').style.display ="block";
            } else {
                document.getElementById('form_modal_div_dpc_oui').style.display ="none";
            }
        }

        $('input[type="checkbox"]').on('change', function() {
        $('input[type="checkbox"]').not(this).prop('checked', false);
    });

    </script>






       
       