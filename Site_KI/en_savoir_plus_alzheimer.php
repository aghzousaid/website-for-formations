        La maladie d’Alzheimer est une maladie neurodégénérative qui touche plus de 900 000 personnes en France. 
        Cette maladie s’accompagne d’une perte de la mémoire, de difficultés de langage, de troubles émotionnels 
        et d’altération de la perception visuelle, mais aussi de perturbations des gestes de la vie quotidienne.
        <br><br>

        Aujourd’hui, il n’existe pas de traitement curatif de la maladie d’Alzheimer. Néanmoins, il est important 
        de prendre en charge la pathologie dès l’apparition des premiers symptômes. Mais pourquoi dépister au plus 
        vite cette maladie ?.<br><br>

        La maladie d’Alzheimer est généralement dépistée de manière tardive. Selon la Haute Autorité de la Santé, 
        l’instauration précoce d’un traitement et une prise en charge médico-sociale permettraient d’assurer 
        une meilleure qualité de vie aux patients et aux aidants. Le diagnostic précoce va également permettre 
        d’accompagner le patient et la famille à un moment où il est à un stade peu avancé de la maladie lui 
        permettant ainsi d’être acteur de celle-ci.<br><br>

        Cette pathologie qui touche généralement les personnes âgées peut être constatée bien plus tôt : “On estime 
        aujourd’hui en France à 33 000 le nombre de patients de moins de 60 ans atteints de la maladie d’Alzheimer” 
        (Fondation pour la recherche sur Alzheimer). Par ailleurs, selon la fondation pour la recherche médicale, 
        la maladie d’Alzheimer deviendrait un fléau en forte progression en 2020. Imaginez-vous un cas diagnostiqué 
        toutes les 3 minutes ? C’est ce qu’affirme cette même fondation, cette maladie devrait ainsi compter plus 
        d’un million de personnes atteintes dans 2 ans..<br><br>

        Il est donc impératif pour les professionnels de santé de diagnostiquer au plus vite afin d’optimiser 
        la prise en charge du patient.<br><br>

        De ce fait, l’organisme de formation Kamedis Institut propose aux gériatres, neurologues et aux médecins 
        généralistes d’optimiser la prise en charge et la qualité de vie du patient.
