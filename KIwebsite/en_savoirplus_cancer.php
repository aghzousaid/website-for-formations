        <p id="m_top_desc_see_more_cancer">
        Le cancer est une pathologie qui résulte d’un dysfonctionnement au niveau des cellules de l’organisme, 
        qui forment par la suite une tumeur. En France, les cancers les plus fréquents sont ceux de la prostate, 
        du sein, du poumon et du colorectal.<br><br>

        Première cause de mortalité chez les hommes en France, cette pathologie a touché 400 000 nouvelles personnes, 
        et a par ailleurs provoqué 150 000 décès.<br><br>

        Cependant, après des années de recherche scientifique, les progrès médicaux sont incessants. 
        D’après le professeur Fabien Calvo, « la mortalité a connu une réduction majeure par rapport à 1980, 
        avec une diminution plus rapide chez les hommes ».<br><br>

        Aujourd’hui, un cancer sur deux est en phase de rémission.
        Malgré ces nombreuses avancées, il est important de dépister et diagnostiquer au plus vite cette pathologie. 
        L’enjeu est grand : limiter les incidences liées au cancer.<br><br>

        Le dépistage précoce de cette pathologie est primordial : “Le dépistage permet de diagnostiquer certains cancers 
        avant l’apparition de symptômes et de pouvoir mieux les soigner, mais aussi de limiter les séquelles liées aux 
        traitements utilisés grâce au repérage et au traitement d’une anomalie qui aurait pu évoluer vers un cancer”. (Roche)
        <br><br>

        Il est donc impératif pour les professionnels de santé d’approfondir leurs connaissances sur les douleurs d’origine 
        cancéreuses et d’être formés sur le dépistage et le diagnostic du cancer.<br><br>

        C’est pourquoi, l’organisme Kamedis Institut a pensé aux professionnels de santé pour leur proposer plusieurs 
        formations autour de cette pathologie.<br><br>

        Nous vous proposons ainsi de vous former autour de la « douleur et cancer », du « cancer de la prostate » et 
        du « cancer de la vessie ».<br><br>

        Ces conférences sont animées par des experts qualifiés dans leur domaine.<br><br>

        Le thème « douleur et cancer » est présenté par le Dr Lemaire, le Dr Minello, le Dr Labreze, le Dr Maindet et 
        le Dr Burnod. Le thème « cancer de la prostate » est quant à lui développé par le Dr Ravery et le Dr Alenda. 
        Enfin, le thème « cancer de la vessie » est animé par le Dr Rouprêt.<br><br>

        Ces formations ont pour vocation de vous former à la prise en charge optimale en vue d’améliorer la qualité du patient.
