        Selon Santé Publique France, une situation alarmante avait été constatée il y a plus d’un moins en Île-de-France.<br> 
        Cette épidémie hivernale s’est étendue sur toute la France touchant alors toutes nos régions. En effet, le nombre
        de personnes se rendant aux urgences en raison de symptômes grippaux s’est multiplié au cours du dernier mois.<br><br>

        Cependant, l’épidémie répandue en France serait moins importante que l’an passé. En 2016, l’activité grippale a causé 
        plus de 14 000 décès.<br><br>

        Si le nombre de décès est aussi élevé, c’est que la grippe touche gravement les personnes les plus vulnérables : <br><br>
        les nourrissons, les personnes âgées, les femmes enceintes, les personnes atteintes de maladies chroniques
        ou d’obésité. Ces personnes, étant plus sensibles aux virus en circulation, sont exposées à des complications
        telles que les infections pulmonaires pouvant causer leur décès.<br><br>

        La vaccination reste le meilleur moyen pour se protéger de la grippe. C’est la solution que les professionnels
        de santé recommandent face à la propagation du virus.<br><br>

        D’autre part, si vous ne souhaitez pas tomber malade durant l’hiver, il est important de respecter certaines 
        mesures d’hygiène afin de limiter la contagion : se laver les mains fréquemment, se moucher dans un mouchoir
        à usage unique, aérer régulièrement son espace, éviter tout contact avec les personnes contaminées.
        <br><br>

